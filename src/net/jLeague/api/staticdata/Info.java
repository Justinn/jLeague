package net.jLeague.api.staticdata;

/**
 * Contains champion information
 * 
 * @author Justin Mintzer
 *
 */
public class Info {
	
	private int attack;
	private int defense;
	private int difficulty;
	private int magic;
	
	public int getAttack() {
		return attack;
	}
	public int getDefense() {
		return defense;
	}
	public int getDifficulty() {
		return difficulty;
	}
	public int getMagic() {
		return magic;
	}

}
