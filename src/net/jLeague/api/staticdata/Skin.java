package net.jLeague.api.staticdata;

/**
 * Contains champion skin data
 * 
 * @author Justin Mintzer
 *
 */
public class Skin {
	
	private int id;
	private String name;
	private int num;
	
	public int getId() {
		return id;
	}
	public String getName() {
		return name;
	}
	public int getNum() {
		return num;
	}

}
