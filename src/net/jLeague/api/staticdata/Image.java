package net.jLeague.api.staticdata;

/**
 * Contains image data
 * 
 * @author Justin Mintzer
 *
 */
public class Image {
	
	private String full;
	private String group;
	private int h;
	private String sprite;
	private int w;
	private int x;
	private int y;
	
	public String getFull() {
		return full;
	}
	public String getGroup() {
		return group;
	}
	public int getH() {
		return h;
	}
	public String getSprite() {
		return sprite;
	}
	public int getW() {
		return w;
	}
	public int getX() {
		return x;
	}
	public int getY() {
		return y;
	}

}
